package day12_warmup;

public class switchScore {
public static void main(String[] args) {
	int score=3;
	switch (score) {
	case 1:
		System.out.println("Score is 1");
		break;
	case 2:
		System.out.println("Score is 2");	
		break;
	case 3:
		System.out.println("Score is 3");
		break;
		default:
			System.out.println("invalid score");
			break;
	}
	
		
}
}
